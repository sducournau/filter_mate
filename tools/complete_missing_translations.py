def get_lang_code(filename):
    """Extract language code from filename (e.g., FilterMate_fr.ts -> fr)"""
    parts = filename.replace('.ts', '').split('_')
    if len(parts) >= 2:
        return parts[-1]
    return None


def update_ts_file(filepath, lang_code):
    """Update missing translations in a .ts file"""
    tree = ET.parse(filepath)
    root = tree.getroot()
    
    updated = 0
    
    for msg in root.iter('message'):
        src = msg.find('source')
        trans = msg.find('translation')
        
        if src is not None and src.text:
            source_text = src.text
            
            # Check if translation is missing or unfinished
            if trans is not None:
                if (trans.get('type') == 'unfinished' or 
                    not trans.text or 
                    not trans.text.strip()):
                    
                    # Look for translation
                    if source_text in TRANSLATIONS:
                        if lang_code in TRANSLATIONS[source_text]:
                            trans.text = TRANSLATIONS[source_text][lang_code]
                            if trans.get('type'):
                                del trans.attrib['type']
                            updated += 1
    
    if updated > 0:
        # Write with proper XML declaration
        tree.write(filepath, encoding='utf-8', xml_declaration=True)
        
        # Fix DOCTYPE (Qt requires it)
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # Add DOCTYPE if missing
        if '<!DOCTYPE TS>' not in content:
            content = content.replace('<?xml version=\'1.0\' encoding=\'utf-8\'?>',
                                      '<?xml version="1.0" encoding="utf-8"?>\n<!DOCTYPE TS>')
        
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(content)
    
    return updated


