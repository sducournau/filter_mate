def add_contexts_to_ts_file(filepath, lang_code):
    """Add new contexts to a .ts file."""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
        
        # Check if new contexts already exist
        if '<name>OptimizationDialog</name>' in content:
            print(f"  {filepath}: contexts already exist, skipping")
            return False
        
        # Find the closing </TS> tag
        if '</TS>' not in content:
            print(f"  ERROR: {filepath}: invalid .ts file format")
            return False
        
        # Build new contexts XML
        new_contexts_xml = ""
        for context_name, messages in NEW_CONTEXTS.items():
            new_contexts_xml += f'<context>\n    <name>{context_name}</name>\n'
            for source_text, translations in messages.items():
                translation = translations.get(lang_code, translations.get('en', source_text))
                # Escape special XML characters
                source_escaped = source_text.replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;')
                translation_escaped = translation.replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;')
                new_contexts_xml += f'    <message>\n'
                new_contexts_xml += f'        <source>{source_escaped}</source>\n'
                new_contexts_xml += f'        <translation>{translation_escaped}</translation>\n'
                new_contexts_xml += f'    </message>\n'
            new_contexts_xml += '</context>\n'
        
        # Insert before </TS>
        new_content = content.replace('</TS>', new_contexts_xml + '</TS>')
        
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        
        print(f"  âœ“ {filepath}: added new contexts")
        return True
        
    except Exception as e:
        print(f"  ERROR: {filepath}: {e}")
        return False


